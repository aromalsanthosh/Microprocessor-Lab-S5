DATA SEGMENT
	CHAR DB 'a'
	STRW DB 'ajay john abraham'
	COUNT EQU ($-STRW)
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE, DS:DATA, ES:DATA
	START:	MOV AX,DATA
		MOV DS,AX
		MOV ES,AX
		MOV CX,00H
		MOV CL,COUNT
		INC CX
		MOV BX,00H
		LEA DI,STRW
		CLD
		MOV AL,CHAR
	L1:	REPNE SCASB
		JCXZ L2
		INC BX
		JMP L1
	L2:	MOV AH,4CH
		INT 21H
CODE ENDS
END START
		